def add(x, y):
    return x + y

def sub(x, y):
    return x - y

def mul(x, y):
    return x * y

def div(x, y):
    return x / y

def mod(x, y):
    return x % y

def sq(x):
    return x ** 2

def sqr(x):
    return x ** (1 / 2)

def reci(x):
    return 1 / x if x != 0 else "Undefined (division by zero)"
