"""Write a program to calculate the bill amount for an item given its quantity sold, value, discount, and tax."""

def calculate_bill(quantity, price_per_item, discount_percent, tax_percent):
    total = quantity * price_per_item
    discount = (discount_percent / 100) * total
    after_discount = total - discount
    tax = (tax_percent / 100) * after_discount
    final_amount = after_discount + tax
    return final_amount

quantity = int(input("Enter quantity sold: "))
price = float(input("Enter price per item: "))
discount = float(input("Enter discount percentage: "))
tax = float(input("Enter tax percentage: "))

bill_amount = calculate_bill(quantity, price, discount, tax)

print("The total bill amount is ₹", bill_amount)

"""Write a python program to calculate a household's electricity bill.
The user should enter the number of units consumed. The charges are as follows:

For the first 100 units: ₹1.50 per unit

For the next 100 units (101–200): ₹2.00 per unit

For units above 200: ₹3.00 per unit

A fixed meter charge of ₹50 is added to the bill.

Display the total amount to be paid with a proper bill format.
"""

def calculate_electricity_bill(units):
    bill = 0
    if units <= 100:
        bill = units * 1.5
    elif units <= 200:
        bill = (100 * 1.5) + ((units - 100) * 2)
    else:
        bill = (100 * 1.5) + (100 * 2) + ((units - 200) * 3)

    bill += 50
    return bill

units = int(input("Enter number of units consumed: "))

total_bill = calculate_electricity_bill(units)

print("\n--- Electricity Bill ---")
print("Units Consumed:", units)
print("Total Amount Payable: ₹", total_bill)

"""Develop a Python program that calculates an employee’s net salary. Accept input for:

Employee name and ID

Number of hours worked

Hourly wage

Calculate the gross salary and deduct 10% as tax. Display a proper salary slip with all details.

"""

def calculate_salary(hours, wage_per_hour):
    gross_salary = hours * wage_per_hour
    tax = 0.10 * gross_salary
    net_salary = gross_salary - tax
    return gross_salary, tax, net_salary

name = input("Enter Employee Name: ")
emp_id = input("Enter Employee ID: ")
hours_worked = float(input("Enter number of hours worked: "))
wage = float(input("Enter hourly wage: "))

gross, tax, net = calculate_salary(hours_worked, wage)

print("\n--- Salary Slip ---")
print("Employee Name:", name)
print("Employee ID:", emp_id)
print("Gross Salary: ₹", gross)
print("Tax Deducted (10%): ₹", tax)
print("Net Salary: ₹", net)

"""Write a Python program to calculate the total cost of movie tickets. Accept:
Number of tickets

Ticket category (Silver: ₹120, Gold: ₹180, Platinum: ₹250)

Add 18% GST to the ticket cost. Display a formatted bill.

"""

def calculate_ticket_cost(tickets, category):
    if category == "silver":
        cost = 120
    elif category == "gold":
        cost = 180
    elif category == "platinum":
        cost = 250
    else:
        cost = 0

    total = tickets * cost
    gst = 0.18 * total
    final_amount = total + gst
    return final_amount

tickets = int(input("Enter number of tickets: "))
category = input("Enter ticket category (Silver/Gold/Platinum): ").lower()

total_cost = calculate_ticket_cost(tickets, category)

print("\n--- Movie Ticket Bill ---")
print("Ticket Category:", category)
print("Number of Tickets:", tickets)
print("Total Cost with GST: ₹", total_cost)

"""Develop a Python program that estimates travel fare based on distance and transport mode.

Input:

Distance (in km)

Mode (Bus: ₹5/km, Train: ₹2/km, Cab: ₹10/km)

Calculate and display the total fare and estimated travel time (assuming constant speeds for each mode).

"""

def calculate_fare(distance, mode):
    if mode == "bus":
        fare = 5 * distance
        speed = 40
    elif mode == "train":
        fare = 2 * distance
        speed = 80
    elif mode == "cab":
        fare = 10 * distance
        speed = 60
    else:
        fare = 0
        speed = 1

    time = distance / speed
    return fare, time

distance = float(input("Enter distance (in km): "))
mode = input("Enter mode of transport (Bus/Train/Cab): ").lower()

fare, travel_time = calculate_fare(distance, mode)

print("\n--- Travel Details ---")
print("Mode of Transport:", mode)
print("Distance:", distance, "km")
print("Total Fare: ₹", fare)
print("Estimated Travel Time:", round(travel_time, 2), "hours")